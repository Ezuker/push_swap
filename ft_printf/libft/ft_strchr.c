#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	c %= 128;
	if (*s == '\0' && c == '\0')
		return ((char *)s);
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
		if (*s == '\0' && c == '\0')
			return ((char *)s);
	}
	return (NULL);
}
